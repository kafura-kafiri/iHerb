def simplify(phrase):
    import string
    translator = str.maketrans('', '', string.punctuation)
    return phrase.translate(translator).lower().strip()

def digify(num):
    digits = {
        '0': '۰',
        '1': '۱',
        '2': '۲',
        '3': '۳',
        '4': '۴',

        '5': '۵',
        '6': '۶',
        '7': '۷',
        '8': '۸',
        '9': '۹',

        '.': '.',
    }
    num = str(num)
    _num = []
    for digit in num:
        _num.append(digits[digit])
    return ''.join(_num)

